# Create list of words that means great
words = ["amazing", "awesome", "excellent", "fantastic", "great"]

# Loop through the list
for word in words:

    print(f"Loops are {word}!")
# the variable word will store one item at a time from the list
# until it gets to the end of the list. And then the loop will
# stop and move on to the next line of code.

# Print final message
print("I <3 loops")
